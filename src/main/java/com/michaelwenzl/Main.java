    public static void main(String[] args) {
        // TODO: IO / Result monad

        // Initialize SDL
        int result = SDL_Init(SDL_INIT_EVERYTHING);
        if (result != 0) {
            throw new IllegalStateException("Unable to initialize SDL library (Error code " + result + "): " + SDL_GetError());
        }

        // Create and init the window
        SDL_Window window = SDL_CreateWindow(
                APP_NAME,
                SDL_WINDOWPOS_CENTERED,
                SDL_WINDOWPOS_CENTERED,
                SCREEN_WIDTH, SCREEN_HEIGHT,
                SDL_WINDOW_SHOWN | SDL_WINDOW_RESIZABLE);
        if (window == null) {
            throw new IllegalStateException("Unable to create SDL window: " + SDL_GetError());
        }

        // Create and init the renderer
        SDL_Renderer renderer = SDL_CreateRenderer(window, -1, SDL_RENDERER_ACCELERATED);
        if (renderer == null) {
            throw new IllegalStateException("Unable to create SDL renderer: " + SDL_GetError());
        }

        // Set color of renderer to green
        SDL_SetRenderDrawColor(renderer, (byte) 30, (byte) 31, (byte) 34, (byte) 255);

        // Clear the window and make it all red
        SDL_RenderClear(renderer);

        // Render the changes above ( which up until now had just happened behind the scenes )
        SDL_RenderPresent(renderer);

        // Start an event loop and react to events
        SDL_Event evt = new SDL_Event();
        boolean shouldRun = true;
        while (shouldRun) {
            shouldRun = runRenderLoop(renderer, evt);
        }

        SDL_Quit();
    }

    private static boolean runRenderLoop(SDL_Renderer renderer, SDL_Event evt) {

        SDL_SetRenderDrawColor(renderer, (byte) 188, (byte) 190, (byte) 196, (byte) 255);

        var rect = new SDL_Rect();
        rect.x = 50;
        rect.y = 50;
        rect.h = 50;
        rect.w = 50;

        SDL_RenderFillRect(renderer, rect);
        SDL_RenderPresent(renderer);

        while (SDL_PollEvent(evt) != 0) {
            switch (evt.type) {
                case SDL_QUIT:
                    return false;
                case SDL_KEYDOWN:
                    if (evt.key.keysym.sym == SDLK_SPACE) {
                        System.out.println("SPACE pressed");
                    }
                    break;
                case SDL_WINDOWEVENT:
                    System.out.println("Window event " + evt.window.event);
                default:
                    break;
            }
        }
        return true;
    }
